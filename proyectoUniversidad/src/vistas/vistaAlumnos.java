    private void btGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardar1ActionPerformed
      String nombre;
      int dni;
      String domicilio;
      LocalDate fechaNac;
      Alumno alumno;
      nombre = tNombre.getText();
      dni = Integer.parseInt(tDni.getText());
      domicilio = tDomicilio.getText();
      fechaNac = LocalDate.parse(tFecha.getText(),DateTimeFormatter.ofPattern("dd/MM/yyyy"));
      alumno = new Alumno(dni,nombre,domicilio,fechaNac);
      ad.guardarAlumno(alumno);
      tId.setText(alumno.getId()+"");
       
    }//GEN-LAST:event_btGuardar1ActionPerformed

