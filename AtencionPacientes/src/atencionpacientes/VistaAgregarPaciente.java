    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        //Construir el vehiculo
        String nombre=jtNombre.getText();
        int dni= Integer.parseInt(jtDni.getText());
        String tipo = jtTipo.getText();
        
        Paciente paciente = new Paciente(nombre, dni, tipo);
        
        int  turno = Integer.parseInt((String)jcTurno.getSelectedItem());
        //Guardar
        if(Menu.getTurno().agregarPaciente(turno, paciente)){
            JOptionPane.showMessageDialog(this, "Paciente Agregado");
        }else {
            JOptionPane.showMessageDialog(this, "Paciente duplicado");
        }
        limpiar();
        
    }//GEN-LAST:event_jbAgregarActionPerformed
    
    private void limpiar(){
        jtNombre.setText("");
        jtDni.setText("");
        jtTipo.setText("");
        jcTurno.setSelectedIndex(0);
       
    }
    
