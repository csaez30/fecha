    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
         int id=Integer.parseInt(tId.getText());
        Alumno alumno=ad.buscarAlumno(id);
        if(alumno!=null){
                tId.setText(alumno.getId()+"");
                tNombre.setText(alumno.getNombre());
                tDni.setText(alumno.getDni()+"");
                tDomicilio.setText(alumno.getDomicilio());
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/YYY");
                 LocalDate fecha=alumno.getFechaNac();
                tFecha.setText(fecha.format(dtf));
                
    }//GEN-LAST:event_btBuscarActionPerformed
    }
