    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        int idAlumno;
        String nombre;
        LocalDate fecNac;
        boolean activo;
        
        Alumno a = new Alumno();
        idAlumno = Integer.parseInt(tIdAlumno.getText());
        a = ad.buscarAlumno(idAlumno);
        
        nombre = a.getNombre();
        fecNac = a.getFecNac();
        activo = a.getActivo();
        
        tNombreAlumno.setText(nombre);
        tFecNacAlumno.setText(fecNac.toString());
        cbActivo.setSelected(activo);
        
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        
        String nombre;
        LocalDate fecNac;
        boolean activo;
        
        Alumno a;
        
        nombre = tNombreAlumno.getText();
        fecNac = LocalDate.parse(tFecNacAlumno.getText());
        activo = cbActivo.isSelected();
         
        a = new Alumno(nombre, fecNac, activo);
        ad.guardarAlumno(a);
        tIdAlumno.setText(a.getId() + "");
    }//GEN-LAST:event_btGuardarActionPerformed

    private void btActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btActualizarActionPerformed
        int idAlumno;
        String nombre;
        LocalDate fecNac;
        boolean activo;
        
        Alumno a;
        
        if(tIdAlumno.getText() != null){
            idAlumno = Integer.parseInt(tIdAlumno.getText());
            nombre = tNombreAlumno.getText();
            fecNac = LocalDate.parse(tFecNacAlumno.getText());
            activo = cbActivo.isSelected();

            a = new Alumno(nombre, fecNac, activo);
            a.setId(idAlumno);

            ad.actualizarAlumno(a);
        }
        
    }//GEN-LAST:event_btActualizarActionPerformed

    private void btBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarActionPerformed
        int idAlumno;
        if(tIdAlumno.getText() != null){
            idAlumno = Integer.parseInt(tIdAlumno.getText());
            ad.borrarAlumno(idAlumno);
        }
            
    }//GEN-LAST:event_btBorrarActionPerformed

