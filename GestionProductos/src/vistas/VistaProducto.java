    private void llenarCombos(){
       CategoriaData cd=new CategoriaData();
       for(Categoria c:cd.obteberCategorias()){
       cbCategoria.addItem(c);
       cbCategoria1.addItem(c);
       
        
       }}
     
    private void llenarTabla(){
    //borraFilasTabla();
    Categoria select= (Categoria)cbCategoria.getSelectedItem();
    if (select!=null){
        
        
    for(Producto p:pd.obtenerProductos()){
        JOptionPane.showMessageDialog(this, "categoria"+p.getCategoria()+" Seleccionada"+select);
      if (p.getCategoria().equals(select))  {
        modelo.addRow(new Object[]{p.getIdProducto(),p.getCodigo(),p.getDescripcion(),p.getPrecio(),p.getCategoria(),p.getStock()});
      JOptionPane.showMessageDialog(this, "entro al if");
          }  
        }
    }
     
    }
    
    private void activarCampos(){
      tCodigo.setEnabled(true);
      tDescrip.setEnabled(true);
      tPrecio.setEnabled(true);
      cbCategoria1.setEnabled(true);
      jStock.setEnabled(true);
         
     }   
     private void limpiarCampos(){
      tCodigo.setText("");
      tDescrip.setText("");
      tPrecio.setText("");
      cbCategoria.setSelectedItem(0);
      jStock.setValue(0);
         
     }   
     private void desactivarCampos(){
      tCodigo.setEnabled(false);
      tDescrip.setEnabled(false);
      tPrecio.setEnabled(false);
      cbCategoria1.setEnabled(false);
      jStock.setEnabled(false);
     
     
     
     }   

